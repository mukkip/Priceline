package com.candidate.chutesladders;

public class Player {

	private String name;
	private int loc;
	private int nloc;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLoc() {
		return loc;
	}
	public void setLoc(int loc) {
		this.loc = loc;
	}
	public int getNloc() {
		return nloc;
	}
	public void setNloc(int nloc) {
		this.nloc = nloc;
	}
	
}
